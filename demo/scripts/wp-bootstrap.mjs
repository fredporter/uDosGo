#!/usr/bin/env node
/**
 * Prepare WordPress for demo steps 8–9: core install (if needed), activate plugin,
 * create Subscriber, pretty permalinks, optional application password for REST.
 *
 * Prereq: `npm run wp:up` and wait until http://127.0.0.1:8080 responds.
 *
 * Env (optional overrides):
 *   WP_DEMO_PUBLIC_URL — site URL WordPress uses (default http://127.0.0.1:8080)
 *   WP_DEMO_ADMIN_USER — default udosadmin
 *   WP_DEMO_ADMIN_PASSWORD — default udosAdminLocal-DEMO-ONLY
 *   WP_DEMO_SUBSCRIBER_USER — default demo
 *   WP_DEMO_SUBSCRIBER_PASSWORD — default demoLocal-DEMO-ONLY
 *
 * Flags:
 *   --no-wait — skip HTTP wait (use if you know WP is already up)
 */

import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "../..");
const composeFile = path.join(ROOT, "infra", "docker", "compose.yml");
const credPath = path.join(ROOT, "demo", ".wp-demo.env");

const siteUrl = (process.env.WP_DEMO_PUBLIC_URL ?? "http://127.0.0.1:8080").replace(
  /\/$/,
  ""
);

async function waitForWordPressContainer(maxMs = 120_000) {
  const start = Date.now();
  console.log("[wp-bootstrap] Waiting for WordPress HTTP…");
  while (Date.now() - start < maxMs) {
    try {
      const r = await fetch(siteUrl, {
        method: "GET",
        redirect: "manual",
        signal: AbortSignal.timeout(5000),
      });
      if (r.ok || r.status === 302 || r.status === 301 || r.status === 500) {
        console.log("[wp-bootstrap] WordPress responded (status", r.status + ").");
        return;
      }
    } catch {
      /* retry */
    }
    await new Promise((res) => setTimeout(res, 2000));
  }
  console.error(
    "[wp-bootstrap] Timed out waiting for WordPress. Run `npm run wp:up` and retry."
  );
  process.exit(1);
}

const skipWait = process.argv.includes("--no-wait");
if (!skipWait) {
  await waitForWordPressContainer();
}
const adminUser = process.env.WP_DEMO_ADMIN_USER ?? "udosadmin";
const adminPass =
  process.env.WP_DEMO_ADMIN_PASSWORD ?? "udosAdminLocal-DEMO-ONLY";
const subUser = process.env.WP_DEMO_SUBSCRIBER_USER ?? "demo";
const subPass =
  process.env.WP_DEMO_SUBSCRIBER_PASSWORD ?? "demoLocal-DEMO-ONLY";

function dockerComposeRun(wpArgs, opts = {}) {
  const { capture = false } = opts;
  const args = [
    "compose",
    "-f",
    composeFile,
    "--profile",
    "bootstrap",
    "run",
    "--rm",
    "-T",
    "-e",
    `WP_BOOT_SITE_URL=${siteUrl}`,
    "-e",
    `WP_BOOT_ADMIN_USER=${adminUser}`,
    "-e",
    `WP_BOOT_ADMIN_PASS=${adminPass}`,
    "-e",
    `WP_BOOT_SUB_USER=${subUser}`,
    "-e",
    `WP_BOOT_SUB_PASS=${subPass}`,
    "wpcli",
    ...wpArgs,
  ];
  return spawnSync("docker", args, {
    cwd: ROOT,
    encoding: capture ? "utf8" : undefined,
    stdio: capture ? "pipe" : "inherit",
  });
}

const inner = `
set -e
export WP_CLI_ALLOW_ROOT=1
cd /var/www/html
if wp core is-installed 2>/dev/null; then
  echo "[wp-bootstrap] Core already installed — skipping wp core install."
else
  echo "[wp-bootstrap] Running wp core install…"
  wp core install --url="$WP_BOOT_SITE_URL" --title="uDOS v3 Demo" \\
    --admin_user="$WP_BOOT_ADMIN_USER" --admin_password="$WP_BOOT_ADMIN_PASS" \\
    --admin_email="admin@udos.local.invalid" --skip-email
fi
wp option update permalink_structure '/%postname%/' --quiet
wp rewrite flush --hard --quiet
if wp plugin is-active udos-empire-local 2>/dev/null; then
  echo "[wp-bootstrap] Plugin already active."
else
  wp plugin activate udos-empire-local
fi
if wp user get "$WP_BOOT_SUB_USER" --field=ID >/dev/null 2>&1; then
  echo "[wp-bootstrap] Subscriber user exists: $WP_BOOT_SUB_USER"
else
  wp user create "$WP_BOOT_SUB_USER" "$WP_BOOT_SUB_USER@udos.local.invalid" \\
    --role=subscriber --user_pass="$WP_BOOT_SUB_PASS"
  echo "[wp-bootstrap] Created subscriber: $WP_BOOT_SUB_USER"
fi
echo "[wp-bootstrap] Done."
`.trim();

const r1 = dockerComposeRun(["sh", "-lc", inner]);
if (r1.status !== 0) {
  console.error(
    "[wp-bootstrap] Failed. Is Docker running? Try: npm run wp:up"
  );
  process.exit(r1.status ?? 1);
}

let appPassword = "";
const r2 = dockerComposeRun(
  [
    "wp",
    "user",
    "application-password",
    "create",
    subUser,
    "udos-demo-link",
    "--porcelain",
  ],
  { capture: true }
);
if (r2.status === 0 && r2.stdout) {
  appPassword = r2.stdout.trim().replace(/\s+/g, "");
} else {
  console.warn(
    "[wp-bootstrap] Could not create application password via WP-CLI (optional). Use browser login + REST cookie, or create one in WP admin → Users → Application passwords."
  );
}

const lines = [
  "# Generated by npm run demo:wp-bootstrap — do not commit (see .gitignore)",
  `UDOS_WP_BASE_URL=${siteUrl}`,
  `WP_DEMO_SUBSCRIBER_USER=${subUser}`,
  `WP_DEMO_SUBSCRIBER_PASSWORD=${subPass}`,
  `WP_DEMO_ADMIN_USER=${adminUser}`,
  `WP_DEMO_RESTRICTED_PATH=/empire-local-restricted/`,
  `WP_DEMO_CONTACT_ID=demo-contact-001`,
];
if (appPassword) {
  lines.push(`WP_LINK_APP_PASSWORD=${appPassword}`);
}

fs.writeFileSync(credPath, `${lines.join("\n")}\n`, "utf8");

console.log("");
console.log("[wp-bootstrap] Credentials written to demo/.wp-demo.env (gitignored).");
console.log("[wp-bootstrap] Subscriber login (step 8):");
console.log(`  URL      ${siteUrl}/wp-login.php`);
console.log(`  Username ${subUser}`);
console.log(`  Password ${subPass}`);
console.log("[wp-bootstrap] Restricted page:");
console.log(`  ${siteUrl}/empire-local-restricted/`);
console.log("[wp-bootstrap] Step 9 — link contact via REST:");
console.log("  npm run demo:wp-link");
console.log("");
